            <h3>LENTES DE CONTACTO MULTIFOCALES</h3>

            <p>Cuando aparece la presbicia o vista cansada, muchas personas creen que no 
                podrán usar lentes de contacto, o que tendrán que usarlas con una gafa adicional
                y desconocen las lentes de contacto multifocales. Pero, ¿cómo funcionan?, ¿me conviene utilizarlas?.

                La presbicia o vista cansada, es un cambio natural del ojo que hace que 
                la visión de cerca se vuelva borrosa, por lo que resulta difícil enfocar en 
                tareas de cerca como leer, utilizar un teléfono móvil o trabajar con el ordenador. Se manifiesta en muchas personas a partir de los 45 años aproximadamente.
                Las lentes de contacto multifocales están diseñadas para utilizar distintas 
                potencias de lente que enfocan la visión a distintas distancias del usuario. 

                ¿Qué son las lentes de contacto multifocales?
                Las lentes de contacto multifocales son unas lentes de contacto con 
                diversas graduaciones en una misma lente. Normalmente incluyen una 
                graduación para ver objetos muy cercanos, una graduación para ver 
                objetos a una distancia normal y una graduación para distancias intermedias. 
                Esta configuración ayuda a las personas con presbicia a corregir sus problemas 
                de visión asociados con la edad, en los que el ojo ya no es capaz de enfocar objetos cercanos.
