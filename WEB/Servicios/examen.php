                <h3>VER BIEN LAS LETRAS MÁS PEQUEÑAS NO EQUIVALE A TENER BUENA VISIÓN...</h3>

                <p>El Examen Optométrico es una evaluación extensa y minuciosa, que incluye un 
                    conjunto de pruebas que evalúan cada habilidad visual por separado, cuyo
                    objetivo es conocer y valorar el estado de salud ocular y visual de nuestros 
                    clientes para las tareas diarias que realizan, comprobar si existe algún problema
                    visual y calcular la mejor solución posible y/o prevenir posibles patologías.

                    Revisiones periódicas de la visión son imprescindibles para poder actuar a 
                    tiempo y prevenir alteraciones en el sistema visual, el cual evoluciona a lo 
                    largo de nuestra vida y se ve afectado, entre otros factores, por nuestros 
                    hábitos diarios; de forma que, cuanto antes detectemos los problemas visuales, 
                    mayor será la probabilidad de tener éxito.</p>

                <label>EL EXAMEN QUE REALIZAMOS CONSTA DE LAS SIGUIENTES PRUEBAS DE DIAGNÓSTICO:</label>
                <ul>
                    <li> Anamnesis: entrevista clínica mediante la que recopilamos datos personales 
                        que comprenden desde antecedentes familiares y personales, signos y síntomas 
                        que experimenta en su día a día o medicación actual, hasta hobbies y actividades 
                        que realiza normalmente. Estos datos se usan para analizar su situación y se
                        incorporan a su historial clínico, proporcionándonos información relevante 
                        para su diagnóstico y tratamiento.
                    </li>
                    <li>
                        Agudeza Visual: mediante esta prueba medimos las letras más pequeñas que usted 
                        es capaz de leer en una tabla estandarizada para este fin. La agudeza visual 
                        puede valorarse a una distancia lejana, media o próxima a fin de conocer el grado de 
                        visión de cerca, intermedia y de lejos. Disponemos también de otros test, por ejemplo, 
                        con dibujos, para la exploración de casos especiales, niños, etc.
                    </li>
                    <li>
                        Autorrefractómetro: nos permite conocer la medición del poder óptico/visual del ojo 
                        mediante un dispositivo automático, es decir, la medida del problema refractivo (existencia 
                        y magnitud de astigmatismos, miopías e hipermetropías), proporcionándonos una medida aproximada 
                        del defecto visual.
                    </li>
                    <li>
                        Retinoscopía: también conocida como el 'estudio de las sombras', consiste en la 
                        neutralización de los reflejos procedentes de la retina con el fin de determinar la refracción y medir el posible error refractivo.
                        Queratometría: en esta prueba determinamos cómo es la córnea (parámetros y regularidad 
                        de la superficie córneal tales como la medida de sus radios de curvatura), lo que es muy 
                        importante, sobre todo, para una correcta adaptación de las lentes de contacto.
                    </li>
                    <li>
                        Refracción subjetiva: con este examen comprobamos que el sujeto refiere comodidad con la 
                        refracción calculada objetivamente o si, por el contrario, hay que realizar algún cambio con el fin de mejorar la comodidad.
                        Medida de la adición para visión de cerca: con esta prueba medimos la presbicia 
                        o comúnmente llamada 'vista cansada'.
                    </li>
                    <li>
                        Visión binocular: desarrollamos la optometría comportamental, valoramos la 
                        visión simultánea de los dos ojos, una vez que ya hemos valorado cada uno por separado, 
                        interpretando de forma simultánea la información proveniente de estos.
                    </li>
                    <li>
                        Acomodación: medimos la capacidad visual de enfocar a cualquier distancia, 
                        es decir, la habilidad de ver claro y definido tan pronto cambiamos la fijación de una 
                        distancia a otra. El control inadecuado de la acomodación producirá visión borrosa, fatiga, 
                        dolores de cabeza o dificultad de enfocar de lejos, que puede ser el inicio o aumento de una 
                        miopía por un exceso de acomodación.
                    </li>
                </ul>
